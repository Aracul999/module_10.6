quantity = int(input('Введите количество чисел: '))  # сколько чисел введёт пользователь

max_sum = 0        # здесь будем хранить максимальную сумму цифр
max_number = 0     # здесь будем хранить число с этой суммой

for _ in range(quantity):  # цикл по количеству чисел
    number = int(input('Введите число: '))  # ввод числа

    temp = number      # копия числа (чтобы не испортить оригинал)
    sum_digits = 0     # сумма цифр текущего числа

    while temp > 0:    # пока число не закончилось
        digit = temp % 10        # берём последнюю цифру
        sum_digits += digit      # добавляем к сумме
        temp = temp // 10        # убираем последнюю цифру

    if sum_digits > max_sum:     # если сумма больше предыдущей
        max_sum = sum_digits     # запоминаем новую максимальную сумму
        max_number = number      # запоминаем само число

print('Число', max_number, 'имеет максимальную сумму цифр:', max_sum)
