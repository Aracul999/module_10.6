quantity = int(input('Введите количество чисел: '))  # спрашиваем, сколько чисел будем проверять
total = 0  # счётчик простых чисел

for number in range(1, quantity + 1):  # цикл: столько раз, сколько чисел ввёл пользователь
    number_1 = int(input('Введите число: '))  # ввод очередного числа

    for number_2 in range(2, number_1):  # перебираем возможные делители от 2 до переменной
        if number_1 % number_2 == 0:  # если число делится без остатка
            break   # значит это НЕ простое → выходим из цикла

    else:  # этот блок выполнится, если break НЕ сработал
        if number_1 > 1:  # проверка: 1 и меньше не считаются простыми
            total += 1   # увеличиваем счётчик простых чисел

print('Количество простых чисел в последовательности:', total)  # вывод результата
